## 1 list 
## A list in Python is an ordered, mutable collection of elements
## they are like arrays in js 
fruits = ["apple" , "mango" ,"banana" , "cherry" , "pineapple"]
print(fruits)

## accesing list 
print(fruits[0])  # Output: apple

print(fruits[1:2])
## Modifying Elements:
fruits[1] = 'blueberry'
print(fruits)

